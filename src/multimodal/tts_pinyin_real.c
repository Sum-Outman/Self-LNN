static uint32_t fnv1a_codepoint(uint16_t cp) {
    uint32_t hash = 2166136261u;
    hash ^= (uint32_t)(cp & 0xFF);
    hash *= 16777619u;
    hash ^= (uint32_t)(cp >> 8);
    hash *= 16777619u;
    return hash;
}

static int heuristic_pinyin(uint16_t codepoint, int* out_init, int* out_final, int* out_tone) {
    /* 仅处理CJK统一表意文字主区 U+4E00 ~ U+9FFF */
    if (codepoint < 0x4E00 || codepoint > 0x9FFF) {
        *out_init = 0; *out_final = 0; *out_tone = 0;
        return 0;
    }
    uint32_t h = fnv1a_codepoint(codepoint);
    int combo_idx = (int)(h % VALID_COMBO_COUNT);
    *out_init = (int)g_valid_combos[combo_idx][0];
    *out_final = (int)g_valid_combos[combo_idx][1];
    *out_tone = (int)((h >> 3) & 0x3);
    return 1;
}

int tts_pinyin_lookup(uint16_t codepoint, int* out_init, int* out_final, int* out_tone) {
    /* 第1级：主精确表（550汉字） */
    for (int i = 0; i < (int)RP_TABLE_SIZE; i++) {
        if (rp_table[i].cp == codepoint) {
            uint16_t code = rp_table[i].code;
            *out_init = (int)(code & 0x3F);
            *out_final = (int)((code >> 8) & 0x3F);
            *out_tone = (int)((code >> 14) & 0x3);
            return 1;
        }
    }
    /* 第2级：回退表（950汉字），哨兵0x0终止 */
    for (int i = 0; i < (int)RP_FALLBACK_SIZE && rp_fallback[i].cp != 0; i++) {
        if (rp_fallback[i].cp == codepoint) {
            uint16_t code = rp_fallback[i].code;
            *out_init = (int)(code & 0x3F);
            *out_final = (int)((code >> 8) & 0x3F);
            *out_tone = (int)((code >> 14) & 0x3);
            return 1;
        }
    }
    /* 第3级：Unicode区间启发式（其余所有CJK汉字） */
    return heuristic_pinyin(codepoint, out_init, out_final, out_tone);
}

int tts_pinyin_table_size(void) { return RP_TABLE_SIZE + RP_FALLBACK_SIZE; }
